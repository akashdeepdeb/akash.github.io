; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

; Copyrights Akashdeep Deb (deb2) 2016 [ECE120 - Lab14 program]

; The structure of lab14 is similar to my lab13 program. Please refer to the documentation of lab13 to understand the structure.
; The program stores a null-terminated string of characters starting from x5002 and prints them in a sequential order.
; We use 3 loops to traverse through the characters to print out the character acc. to characters stored in x5000 and x5001.
; The way the program works is by first printing out the first row of the first character and then printing the first row of
; the second character till it reaches the the end of the first row of all the character thatâ€™s when it detects the null character,
; telling it to move to the second row of the first character and so on. 

; LABELS

; BIT0 - stores address val x5000
; BIT1 - stores address val x5001
; CHAR - stores address val x5002
; ASCII_NL - stores New line ASCII val
; FONT_DATA - has info regarding all characters

; REGISTERS

; R1 - row counter init 16
; R2 - moves R3 current loc. to next char - char counter 
; R3 - location of characters to be printed
; R4 - column counter init 8
; R5 - stores FONT_DATA location
; R6 - loads font_data line that has character data 



.ORIG X3000

; initializing registers

	LEA R5, FONT_DATA	; load FONT_DATA loc
	AND R1, R1, #0		; clear R1
	ADD R1, R1, #15		; initialize row counter
	
; start with program

NEXT_ROW 
	AND R2, R2, #0		; Each row R2 becomes 0 - tracks char printed

NEXT_CHAR 
	LD R3, CHAR		; loads location x5002
	ADD R3, R3, R2		; points to which sprite to be printed
	LDR R6, R3, #0		; accesses char = M[x5002+R2]

	BRz CHAR_FIN		; R6 == 0? 1 char done, 0 cont

	ADD R6, R6, R6		; R6 left shift (1)
	ADD R6, R6, R6		; R6 left shift (2)
	ADD R6, R6, R6		; R6 left shift (3)
	ADD R6, R6, R6		; R6 final left shift (4)

	ADD R6, R6, R5		; R6 gets (FONT_DATA + M[x5002+R2]*16)
	LDR R6, R6, #0		; R6 <- M[R6]

	AND R4, R4, #0		; clear col count
	ADD R4, R4, #7		; init col count
		
NEXT_COL 
	BRn COL_FIN		; if R4 is negative col over
	ADD R6, R6, #0		; set cont. codes using R6
	BRn SKIP		; (R6 first bit 1?) => R6 < 0? 1 skip, 0 cont

	LDI R0, BIT0		; load bit1 character into R0
	OUT			; print M[x5000]
	BRnzp UPDATE		; unconditional jump to UPDATE

SKIP 
	LDI R0, BIT1		; load bit0 character into R0
	OUT 			; print M[x5001]

UPDATE 
	ADD R6, R6, R6		; left shift R6
	ADD R4, R4, #-1		; decrement col count
	BRnzp NEXT_COL

COL_FIN 
	ADD R2, R2, #1		; add 1 to move to next sprite char 
	BRnzp NEXT_CHAR		; unconditional jump to next sprite char

CHAR_FIN
	ADD R1, R1, #-1		; move to next row
	BRn DONE		; If R1<0, prog_over
	LD R0, ASCII_NL		; R0 gets newline
	OUT			; print newline
	ADD R5, R5, #1		; move to next sequence of bits
	BRnzp NEXT_ROW		; unconditional jump to next_row

DONE 
	HALT			; TRAP x25


BIT0 .FILL x5000
BIT1 .FILL X5001
CHAR .FILL x5002
ASCII_NL .FILL xA

