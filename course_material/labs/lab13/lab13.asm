; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

; Copyrights Akashdeep Deb (deb2) 2016 [ECE120 - Lab13 program]

; The following program traverses through 16 rows of FONT_DATA to print the appropriate output
; The program extensively uses left-shift to multiply by powers of 2 and to get the most significant
; bit of a register when setting condition codes. 
; The program has 2 loops, one outer loop (traversed with R3) that counts the rows and an inner loop (traversed with R4)
; to count the columns. This allows the program to find 1s and 0s in each row and appropriately print output.

; ALGORITHM FOR BINARY MANIPULATION

; We use the fact that when the register containing the values of the rows of
; the character, is added to itself, it is left shifted. This allows us to set
; the condition codes where the most significant bit represents one bit from 16 bits.
; eg. Let R6 have 110 - if we check nzp now, it is '1', implying a negative condition.
; R6 <- R6 + R6, we have 110 + 110 = 100, whose most significant bit is '1', implying 
; a negative condition. Adding R6 <- R6+R6, we have 100 + 110 = 010, which has the most
; significant bit as '0', implying a positive condition. We can stop now, since we have 
; successfully gone through all of R6's initial bits '1', '1', and '0'. Hence, this 
; is a great way, to condition that code so that we can manipulate the bits to our needs
; where in this code, we print M[x5000] when a '0' is detected and M[x5001] when a '1'
; is detected. 


; LABELS

; BIT0 - stores address val x5000
; BIT1 - stores address val x5001
; CHAR - stores address val x5002
; ASCII_NL - stores New line ASCII val
; FONT_DATA - has info regarding all characters

; REGISTERS

; R1 - pointer to the first row of the character (FONT_DATA + M[x5002]*x0010)
; R2 - value stored at R1 to be used for bit manipulation (M[R1])
; R3 - row counter init 16
; R4 - column counter init 8
; R5 - calculates 4 times left shifted M[x5002] to give M[x5002]*16


.ORIG x3000

; initialize registers (R1, R2, R3, R4)

	LDI R5, CHAR		; R5 <- M[x5002]
	ADD R5, R5, R5		; R5 left shift (1)
	ADD R5, R5, R5  	; R5 left shift (2)
	ADD R5, R5, R5  	; R5 left shift (3)
	ADD R5, R5, R5  	; R5 final left shift (4)
	
	LEA R1, FONT_DATA	; R5 <- FONT_DATA
	ADD R1, R1, R5		; R1 gets (FONT_DATA + M[x5002]*16)
	

	AND R3, R3, #0 		; clear R3
	ADD R3, R3, #15		; for 16 rows


; start with program 
	
NEXT_ROW			
	LDR R2, R1, #0		; Loads first row value to R2
	ADD R3, R3, #0
	BRn DONE		; R3 == 0? 1 halt, 0 cont

	AND R4, R4, #0		; clear R4
	ADD R4, R4, #8		; for 8 columns
	

NEXT_COL			
	ADD R4, R4, #0	
	BRz DONE_ROW		; R4 == 0? 1 row over, 0 cont

	ADD R2, R2, #0
	BRzp BIT_0		; chk 1st bit of R2 is -ve if 1, +ve or 0, if 0
	LDI R0, BIT1		; load bit1 character into R0
	OUT
	BRnzp UPDATE

BIT_0 
	LDI R0, BIT0		; load bit0 character into R0
	OUT
	
UPDATE
	ADD R2, R2, R2		; left shift R2
	ADD R4, R4, #-1		; decrement R4
	BRnzp NEXT_COL

DONE_ROW			
	LD R0, ASCII_NL		; print new line. loads new line to R0
	OUT
	ADD R1, R1, #1		; moves to next row in FONT_DATA
	ADD R3, R3, #-1		; decrement row counter
	BRnzp NEXT_ROW	

DONE
	HALT
	

BIT0 	.FILL x5000
BIT1 	.FILL x5001
CHAR 	.FILL x5002
ASCII_NL .FILL xA

